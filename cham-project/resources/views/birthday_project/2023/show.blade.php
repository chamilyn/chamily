                                singleValue.textContent = formattedNumber;
                            } else {
                                singleValue.textContent = startValue;
                            }
                            
                            // Clearing the interval
                            if (startValue == endValue) {
                                clearInterval(counter);
                            }
                        }, duration);
                    });
                }
